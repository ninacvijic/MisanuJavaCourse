package cvija;

public class Voce {

	protected String naziv;
	protected String boja;
	protected double cena;
	protected double kolicina;

	public Voce(String naziv, String boja, double cena, double kolicina) {
		this.naziv = naziv;
		this.boja = boja;
		this.cena = cena;
		this.kolicina = kolicina;
	}

}
