package UUP;
/*
Korišćenje Pitagorine teoreme za izračunavanje dužine hipotenuze na osnovu dužina kateta trougla.
*/
public class Pitagora {
	
	public static void main(String[] args) {
		 double x, y, z;
		 x = 3; 
		 y = 4; 
		 z = Math.sqrt(x * x + y * y); 
		 System.out.println("Dužina hipotenuze je " + z); 
	}

}
